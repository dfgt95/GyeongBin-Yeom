import turtle as t

def drow_mid():     # 'ㅕ'를 그리는 함수 정의
    t.down()
    t.forward(30)
    t.left(90)
    t.backward(30)
    t.forward(110)
    t.backward(30)
    t.left(90)
    t.forward(30)
    t.up()



t.up()
t.goto(-200,150)
t.down()
t.circle(30)
t.up()
t.forward(90)
drow_mid()          # 함수 호출
t.goto(-200,90)
t.down()
t.left(90)
t.forward(40)
t.left(90)
t.forward(90)
t.left(90)
t.forward(40)
t.left(90)
t.forward(90)

t.up()
t.goto(-30,210)
t.down()
t.right(180)
t.forward(80)
t.right(110)
t.forward(80)
t.up()
t.goto(80,160)
t.left(110)
drow_mid()          # 함수 호출
t.goto(45,90)
t.down()
t.circle(20)
t.up()
t.goto(180,210)
t.down()
t.left(90)
t.forward(70)
t.left(90)
t.forward(90)
t.left(90)
t.forward(70)
t.backward(30)
t.left(90)
t.forward(90)
t.up()
t.goto(300,230)
t.left(90)
t.down()
t.forward(130)
t.up()
t.goto(200,110)
t.down()
t.forward(60)
t.left(90)
t.forward(120)

t.exitonclick()
